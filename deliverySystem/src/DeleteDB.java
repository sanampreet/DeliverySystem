    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try
        {
            Connection myconnection;
            String path="jdbc:mysql://localhost/";
            String place="deliverysystem";
            String username="root";
            String password="";
            myconnection=DriverManager.getConnection(path+place,username,password);
            try
            {
                
                String q="select * from addboy where  Id=?";
                PreparedStatement mystatement=myconnection.prepareStatement(q);
                  mystatement.setString(1, jFormattedTextField2.getText());
                  ResultSet myresult=mystatement.executeQuery();
                 if(myresult.next())
                 {                 
                     jTextField1.setText(myresult.getString("name"));
                     jFormattedTextField1.setText(myresult.getString("contact"));
                     jFormattedTextField3.setText(myresult.getString("age"));
                     jDateChooser1.setDate(myresult.getDate("joining date"));
                     jTextArea1.setText(myresult.getString("address"));
                     jTextField3.setText(myresult.getString("working hours"));        
                     jFormattedTextField4.setText(myresult.getString("salary"));
                     
                 }
               
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(rootPane, "Error"+e.getMessage());
            }
            finally
            {
                myconnection.close();
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(rootPane, "Error"+e.getMessage());
            
        }


        
    }//GEN-LAST:event_jButton3ActionPerformed


